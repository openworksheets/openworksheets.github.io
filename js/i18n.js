let _lang = null;

export function getLang() {
  if (_lang) return _lang;
  const stored = localStorage.getItem('wpf-lang');
  if (stored && LANGS_AVAILABLE.find(l => l.code === stored)) {
    _lang = stored; return _lang;
  }
  const browser = (navigator.language || 'es').slice(0, 2).toLowerCase();
  _lang = LANGS_AVAILABLE.find(l => l.code === browser) ? browser : 'es';
  return _lang;
}

export function setLang(code, { save = true, reload = true } = {}) {
  if (!LANGS_AVAILABLE.find(l => l.code === code)) code = 'es';
  _lang = code;
  if (save) localStorage.setItem('wpf-lang', code);
  if (reload) window.location.reload();
}

export function t(key, vars = {}) {
  const lang = getLang();
  const str = (STRINGS[lang]?.[key]) ?? (STRINGS.es?.[key]) ?? key;
  return str.replace(/\{(\w+)\}/g, (_, k) => (vars[k] !== undefined ? String(vars[k]) : '{' + k + '}'));
}

// ── DOM ──────────────────────────────────────────────────────────────────────

export function applyI18n() {
  document.documentElement.lang = getLang();
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    el.innerHTML = t(el.dataset.i18nHtml);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = t(el.dataset.i18nTitle);
  });
}

export function initLangSelector() {
  const sel = document.getElementById('selLang');
  if (!sel) return;
  sel.value = getLang();
  sel.addEventListener('change', () => setLang(sel.value));
}
